#Working with integers
x=10
y=-5
sum=x+y
print(f"Sum: {sum}")


#Working with floating-point number
a=2.5
b=0.5
product = a*b
print(f"Product: {product}")


#Integer division
result=10//3
print(f"Integer division result: {result}")


#Combining integer and float division
float_result = 10.0 // 3
print(f"Float result from integer division: {float_result}")
